// JS Data Types - Beginner Level

// Number
let age = 20;
console.log("Age (Number):", age);

// String
let name = "Ramu";
console.log("Name (String):", name);

// Boolean
let isStudent = true;
console.log("isStudent (Boolean):", isStudent);

// Undefined
let city;
console.log("City (Undefined):", city);

// Null
let emptyValue = null;
console.log("Empty Value (Null):", emptyValue);

// Object
let person = { firstName: "Ramu", lastName: "Kumar", age: 20 };
console.log("Person (Object):", person);

// Array
let colors = ["red", "green", "blue"];
console.log("Colors (Array):", colors);
